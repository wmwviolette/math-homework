 const add = (a, b) => {
  return a + b;
};

const subtract = (a, b) => {
  return a - b;
};

const multiply = (a, b) => {
  return a * b;
};

const divide = (a, b) => {
  return a / b;
};

// Add two numbers
add(2, 3);

// Subtract one number from another
subtract(4, 2);

// Multiply two numbers
multiply(5, 2);

// Divide one number by another
divide(10, 2);